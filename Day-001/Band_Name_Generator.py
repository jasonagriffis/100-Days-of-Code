# Print a welcome message for the program
print("Welcome to the Band Name Generator!")

# Ask for input on a city and pet name to use for the band name
city_name = input("What city did you grow up in?\n")
pet_name = input("What is your pets name?\n")

# Combine the input variables to create the band name,
# then print them out to the user
